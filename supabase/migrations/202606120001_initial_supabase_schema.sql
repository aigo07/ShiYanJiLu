create or replace function public.dashboard_stats(
  p_completed_days integer default 7,
  p_top_n integer default 5
)
returns jsonb
language plpgsql
stable
security invoker
set search_path = public
as $$
declare
  completed_since timestamptz := now() - make_interval(days => least(greatest(coalesce(p_completed_days, 7), 1), 3650));
  week_start timestamptz := date_trunc('week', now());
  top_n integer := least(greatest(coalesce(p_top_n, 5), 1), 20);
  a_total integer;
  b_total integer;
  a_top_sum integer;
  b_top_sum integer;
  a_top jsonb;
  b_top jsonb;
begin
  with a_rows as (
    select r.curing_agent_a_id as curing_agent_id, ca.name, count(*)::integer as count
    from public.records r
    join public.curing_agents ca on ca.id = r.curing_agent_a_id
    group by r.curing_agent_a_id, ca.name
    order by count(*) desc
    limit top_n
  ),
  b_rows as (
    select r.curing_agent_b_id as curing_agent_id, ca.name, count(*)::integer as count
    from public.records r
    join public.curing_agents ca on ca.id = r.curing_agent_b_id
    group by r.curing_agent_b_id, ca.name
    order by count(*) desc
    limit top_n
  )
  select
    coalesce((select jsonb_agg(to_jsonb(a_rows)) from a_rows), '[]'::jsonb),
    coalesce((select jsonb_agg(to_jsonb(b_rows)) from b_rows), '[]'::jsonb),
    coalesce((select sum(count)::integer from a_rows), 0),
    coalesce((select sum(count)::integer from b_rows), 0)
  into a_top, b_top, a_top_sum, b_top_sum;

  select count(*)::integer into a_total from public.records;
  select count(*)::integer into b_total from public.records;

  return jsonb_build_object(
    'curing_agents_a_top', a_top,
    'curing_agents_a_other_count', greatest(0, a_total - a_top_sum),
    'curing_agents_b_top', b_top,
    'curing_agents_b_other_count', greatest(0, b_total - b_top_sum),
    'ongoing_experiments_count', (select count(*)::integer from public.experiments where status = '进行中'),
    'completed_experiments_count', (select count(*)::integer from public.experiments where status = '已结束' and end_at >= completed_since),
    'new_experiments_this_week_count', (select count(*)::integer from public.experiments where created_at >= week_start)
  );
end;
$$;

create or replace function public.delete_experiment(p_experiment_id integer)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  before_row jsonb;
  actor_uuid uuid;
  actor_display_name text;
begin
  if not public.has_app_role(array['admin', 'editor']) then
    raise exception 'Forbidden' using errcode = '42501';
  end if;

  select to_jsonb(e)
  into before_row
  from public.experiments e
  where e.id = p_experiment_id;

  if before_row is null then
    raise exception 'Experiment not found' using errcode = 'P0002';
  end if;

  actor_uuid := auth.uid();
  select p.display_name
  into actor_display_name
  from public.profiles p
  where p.id = actor_uuid;

  perform set_config('app.suppress_audit', 'on', true);
  update public.experiments
  set final_record_id = null
  where id = p_experiment_id;

  delete from public.experiments
  where id = p_experiment_id;
  perform set_config('app.suppress_audit', 'off', true);

  insert into public.audit_events (
    actor_type,
    actor_id,
    actor_name,
    action,
    entity_type,
    entity_id,
    request_id,
    ip,
    user_agent,
    "before"
  )
  values (
    'user',
    actor_uuid::text,
    actor_display_name,
    'experiment.delete',
    'experiment',
    p_experiment_id::text,
    public.request_header('x-request-id'),
    public.request_header('x-forwarded-for'),
    public.request_header('user-agent'),
    before_row
  );
end;
$$;

