create or replace function public.list_experiments(
  p_process_type_id integer default null,
  p_status text default null,
  p_curing_agent_id integer default null,
  p_q text default null,
  p_limit integer default 50,
  p_offset integer default 0
)
returns setof public.experiments
language sql
stable
security invoker
set search_path = public
as $$
  select e.*
  from public.experiments e
  where (p_process_type_id is null or e.process_type_id = p_process_type_id)
    and (p_status is null or e.status = p_status)
    and (
      p_curing_agent_id is null
      or exists (
        select 1
        from public.records r
        where r.experiment_id = e.id
          and (r.curing_agent_a_id = p_curing_agent_id or r.curing_agent_b_id = p_curing_agent_id)
      )
    )
    and (
      nullif(trim(coalesce(p_q, '')), '') is null
      or e.customer_name ilike '%' || trim(p_q) || '%'
      or e.project_no ilike '%' || trim(p_q) || '%'
      or e.silicone_model ilike '%' || trim(p_q) || '%'
    )
  order by e.id desc
  limit least(greatest(coalesce(p_limit, 50), 1), 200)
  offset greatest(coalesce(p_offset, 0), 0)
$$;

