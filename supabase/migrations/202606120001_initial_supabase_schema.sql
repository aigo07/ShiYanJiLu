create or replace function public.validate_curing_agent_composition()
returns trigger
language plpgsql
as $$
declare
  item jsonb;
  material_id integer;
  mass_pct double precision;
  total double precision := 0;
  material_ids integer[] := '{}';
begin
  if new.composition is null then
    return new;
  end if;

  if jsonb_typeof(new.composition) <> 'array' then
    raise exception 'composition must be an array' using errcode = '23514';
  end if;

  if jsonb_array_length(new.composition) = 0 then
    new.composition = null;
    return new;
  end if;

  for item in select value from jsonb_array_elements(new.composition)
  loop
    material_id := nullif(item ->> 'material_id', '')::integer;
    mass_pct := nullif(item ->> 'mass_pct', '')::double precision;

    if material_id is null or mass_pct is null then
      raise exception 'composition rows require material_id and mass_pct' using errcode = '23514';
    end if;

    if mass_pct < 0 or mass_pct > 100 then
      raise exception 'composition mass_pct must be between 0 and 100' using errcode = '23514';
    end if;

    if material_id = any(material_ids) then
      raise exception 'composition material_id duplicated' using errcode = '23514';
    end if;

    if not exists (select 1 from public.materials m where m.id = material_id) then
      raise exception 'composition material_id not found: %', material_id using errcode = '23503';
    end if;

    material_ids := array_append(material_ids, material_id);
    total := total + mass_pct;
  end loop;

  if abs(total - 100) > 0.000001 then
    raise exception 'composition mass_pct must sum to 100' using errcode = '23514';
  end if;

  return new;
end;
$$;

