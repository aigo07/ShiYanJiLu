language sql
stable
security invoker
set search_path = public
as $$
  select
    r.id,
    r.experiment_id,
    coalesce(r.process_type_id, e.process_type_id),
    pt.name::text,
    e.final_record_id = r.id,
    r.curing_agent_a_id,
    ca.name::text,
    r.ratio_a_pct,
    r.curing_agent_b_id,
    cb.name::text,
    r.ratio_b_pct,
    r.ml,
    r.mh,
    r.t10_sec,
    r.t90_sec,
    r.bubble_grade,
    r.note,
    r.created_at,
    r.updated_at,
    e.customer_name::text,
    e.project_no::text,
    e.silicone_model::text,
    e.debug_goal,
    e.start_at,
    e.end_at,
    e.note,
    e.created_at,
    e.updated_at
  from public.records r
  join public.experiments e on e.id = r.experiment_id
  left join public.process_types pt on pt.id = coalesce(r.process_type_id, e.process_type_id)
  join public.curing_agents ca on ca.id = r.curing_agent_a_id
  join public.curing_agents cb on cb.id = r.curing_agent_b_id
  where (p_experiment_id is null or r.experiment_id = p_experiment_id)
    and (p_process_type_id is null or coalesce(r.process_type_id, e.process_type_id) = p_process_type_id)
    and (p_status is null or e.status = p_status)
    and (p_curing_agent_id is null or r.curing_agent_a_id = p_curing_agent_id or r.curing_agent_b_id = p_curing_agent_id)
    and (nullif(trim(coalesce(p_customer_q, '')), '') is null or e.customer_name ilike '%' || trim(p_customer_q) || '%')
    and (nullif(trim(coalesce(p_silicone_model_q, '')), '') is null or e.silicone_model ilike '%' || trim(p_silicone_model_q) || '%')
    and (nullif(trim(coalesce(p_project_no_q, '')), '') is null or e.project_no ilike '%' || trim(p_project_no_q) || '%')
    and (p_start_from is null or e.start_at >= p_start_from)
    and (p_start_to is null or e.start_at <= p_start_to)
    and (p_only_final is false or e.final_record_id = r.id)
  order by r.id desc
  limit least(greatest(coalesce(p_limit, 20000), 1), 20000)
$$;

