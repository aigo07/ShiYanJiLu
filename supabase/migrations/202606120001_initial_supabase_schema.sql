create or replace function public.normalize_experiment_status()
returns trigger
language plpgsql
as $$
begin
  if new.status is null then
    new.status = '待开始';
  end if;

  if new.status = '已结束' then
    if new.end_at is null or new.end_at = new.start_at then
      new.end_at = now();
    end if;
  else
    new.end_at = null;
  end if;

  return new;
end;
$$;

