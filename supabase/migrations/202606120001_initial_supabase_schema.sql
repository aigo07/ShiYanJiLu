create or replace function public.jsonb_diff(before_row jsonb, after_row jsonb)
returns jsonb
language sql
immutable
as $$
  select coalesce(
    jsonb_object_agg(key, jsonb_build_object('from', old_value, 'to', new_value)),
    '{}'::jsonb
  )
  from (
    select coalesce(n.key, o.key) as key, o.value as old_value, n.value as new_value
    from jsonb_each(coalesce(after_row, '{}'::jsonb)) n
    full join jsonb_each(coalesce(before_row, '{}'::jsonb)) o using (key)
    where n.value is distinct from o.value
  ) d
$$;

create or replace function public.audit_business_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  before_row jsonb;
  after_row jsonb;
  action_name text;
  actor_display_name text;
  actor_uuid uuid;
  entity_id_text text;
begin
  if current_setting('app.suppress_audit', true) = 'on' then
    if tg_op = 'DELETE' then
      return old;
    end if;
    return new;
  end if;

  actor_uuid := auth.uid();
  select p.display_name
  into actor_display_name
  from public.profiles p
  where p.id = actor_uuid;

  if tg_op = 'INSERT' then
    after_row := to_jsonb(new);
    action_name := tg_table_name || '.create';
    entity_id_text := new.id::text;
  elsif tg_op = 'UPDATE' then
    before_row := to_jsonb(old);
    after_row := to_jsonb(new);
    action_name := tg_table_name || '.update';
    entity_id_text := new.id::text;
  else
    before_row := to_jsonb(old);
    action_name := tg_table_name || '.delete';
    entity_id_text := old.id::text;
  end if;

  action_name := replace(action_name, 'experiments.', 'experiment.');
  action_name := replace(action_name, 'records.', 'record.');
  action_name := replace(action_name, 'materials.', 'material.');
  action_name := replace(action_name, 'curing_agents.', 'curing_agent.');

  insert into public.audit_events (
    actor_type,
    actor_id,
    actor_name,
    action,
    entity_type,
    entity_id,
    request_id,
    ip,
    user_agent,
    "before",
    "after",
    diff
  )
  values (
    'user',
    actor_uuid::text,
    actor_display_name,
    action_name,
    case tg_table_name
      when 'curing_agents' then 'curing_agent'
      else regexp_replace(tg_table_name, 's$', '')
    end,
    entity_id_text,
    public.request_header('x-request-id'),
    public.request_header('x-forwarded-for'),
    public.request_header('user-agent'),
    before_row,
    after_row,
    case when tg_op = 'UPDATE' then public.jsonb_diff(before_row, after_row) else null end
  );

  if tg_op = 'DELETE' then
    return old;
  end if;
  return new;
end;
$$;

