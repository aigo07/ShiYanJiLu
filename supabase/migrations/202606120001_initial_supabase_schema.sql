create or replace function public.validate_experiment_final_record()
returns trigger
language plpgsql
as $$
declare
  record_experiment_id integer;
begin
  if new.final_record_id is null then
    return new;
  end if;

  select r.experiment_id
  into record_experiment_id
  from public.records r
  where r.id = new.final_record_id;

  if record_experiment_id is null then
    raise exception 'final_record_id not found' using errcode = '23503';
  end if;

  if record_experiment_id <> new.id then
    raise exception 'final_record_id must belong to the experiment' using errcode = '23514';
  end if;

  return new;
end;
$$;

